function populateCurrencySelect(filter = "") {
  const normalizedFilter = filter.trim().toLowerCase();
  const visibleCodes = Object.keys(currencyData).filter((code) => {
    const meta = currencyMeta[code] || { name: code };
    return `${code} ${meta.name}`.toLowerCase().includes(normalizedFilter);
  });

  currencySelect.innerHTML = visibleCodes
    .map((code) => {
      const meta = currencyMeta[code] || { name: code };
      const selected = selectedCurrencies.includes(code) ? "selected" : "";
      return `<option value="${code}" ${selected}>${code} — ${meta.name}</option>`;
    })
    .join("");
}

function populateYearSelects() {
  const years = getAllYears();
  startYearSelect.innerHTML = years.map((year) => `<option value="${year}">${year}</option>`).join("");
  endYearSelect.innerHTML = years.map((year) => `<option value="${year}">${year}</option>`).join("");
  startYearSelect.value = years.includes(1947) ? "1947" : String(years[0]);
  endYearSelect.value = String(years.at(-1));
}

function handleYearRangeChange() {
  if (Number(startYearSelect.value) > Number(endYearSelect.value)) {
    [startYearSelect.value, endYearSelect.value] = [endYearSelect.value, startYearSelect.value];
  }
  updateDashboard();
}

function updateDashboard() {
  const timestamps = getSelectedTimestamps();
  const rows = buildRows(timestamps, selectedCurrencies);
  updateHeadlineQuote(rows);
  updateModeDescription();
  renderChart(timestamps, rows);
  renderTable(rows);
  renderInsights(rows);
}

function renderChart(timestamps, rows) {
  const datasets = selectedCurrencies.map((code) => {
    const points = rows.map((row) => {
      const rawRate = row[code];
      if (rawRate === null || rawRate === undefined) return null;
      if (selectedMode === "normalized") {
        const startRate = findFirstRate(rows, code);
        return startRate ? (rawRate / startRate) * 100 : null;
      }
      return rawRate;
    });

    return {
      label: code,
      data: points,
      borderColor: getCurrencyColor(code),
      backgroundColor: getCurrencyColor(code),
      borderWidth: 2,
      pointRadius: 3,
      pointHoverRadius: 6,
      tension: 0.25,
      spanGaps: true
    };
  });

  if (!window.Chart) {
    renderCanvasFallback(years, datasets);
    return;
  }

  if (chart) chart.destroy();

  chart = new Chart(chartCanvas, {
    type: "line",
    data: { labels: timestamps.map(formatTimestampLabel), datasets },
    options: {
      responsive: true,
      maintainAspectRatio: false,
      interaction: { mode: "index", intersect: false },
      plugins: {
        legend: {
          labels: { color: "#d7e2f0", usePointStyle: true, boxWidth: 8 }
        },
        tooltip: {
          callbacks: {
            label(context) {
              const value = context.parsed.y;
              if (value === null) return `${context.dataset.label}: no data`;
              const suffix = selectedMode === "normalized" ? " index" : "";
              return `${context.dataset.label}: ${formatRate(value)}${suffix}`;
            }
          }
        }
      },
      scales: {
        x: {
          grid: { color: "rgba(143, 161, 184, 0.12)" },
          ticks: { color: "#8fa1b8" }
        },
        y: {
          type: selectedMode === "log" ? "logarithmic" : "linear",
          beginAtZero: selectedMode !== "log",
          title: {
            display: true,
            text: selectedMode === "normalized" ? "Index (start year = 100)" : "1 USD = X currency",
            color: "#8fa1b8"
          },
          grid: { color: "rgba(143, 161, 184, 0.12)" },
          ticks: {
            color: "#8fa1b8",
            callback: (value) => formatRate(value)
          }
        }
      }
    }
  });
}

function renderCanvasFallback(timestamps, datasets) {
  const context = chartCanvas.getContext("2d");
  const rect = chartCanvas.parentElement.getBoundingClientRect();
  const scale = window.devicePixelRatio || 1;
  chartCanvas.width = rect.width * scale;
  chartCanvas.height = rect.height * scale;
  context.scale(scale, scale);
  context.clearRect(0, 0, rect.width, rect.height);

  const padding = { top: 24, right: 28, bottom: 46, left: 64 };
  const width = rect.width - padding.left - padding.right;
  const height = rect.height - padding.top - padding.bottom;
  const allValues = datasets.flatMap((set) => set.data).filter((value) => value !== null && value > 0);
  const min = selectedMode === "log" ? Math.min(...allValues) : 0;
  const max = Math.max(...allValues);
  const yValue = (value) => {
    if (selectedMode === "log") {
      return padding.top + height - ((Math.log10(value) - Math.log10(min)) / (Math.log10(max) - Math.log10(min))) * height;
    }
    return padding.top + height - ((value - min) / (max - min || 1)) * height;
  };

  context.strokeStyle = "rgba(143, 161, 184, 0.16)";
  context.fillStyle = "#8fa1b8";
  context.font = "12px system-ui";

  for (let index = 0; index <= 4; index++) {
    const y = padding.top + (height / 4) * index;
    context.beginPath();
    context.moveTo(padding.left, y);
    context.lineTo(padding.left + width, y);
    context.stroke();
  }

  datasets.forEach((set) => {
    context.strokeStyle = set.borderColor;
    context.lineWidth = 2;
    context.beginPath();
    set.data.forEach((value, index) => {
      if (value === null || value <= 0) return;
      const x = padding.left + (width / Math.max(timestamps.length - 1, 1)) * index;
      const y = yValue(value);
      if (index === 0) context.moveTo(x, y);
      else context.lineTo(x, y);
    });
    context.stroke();
  });

  context.fillText(formatTimestampLabel(timestamps[0]), padding.left, rect.height - 18);
  context.fillText(formatTimestampLabel(timestamps.at(-1)), padding.left + width - 56, rect.height - 18);
  context.fillStyle = "#d7e2f0";
  context.fillText("Chart.js unavailable — using simple canvas fallback", padding.left, 18);
}

function renderTable(rows) {
  const header = ["Date", ...selectedCurrencies];
  const body = rows
    .map((row) => {
      const cells = selectedCurrencies.map((code) => `<td>${row[code] === null ? "—" : formatRate(row[code])}</td>`);
      return `<tr><td>${formatTimestampLabel(row.timestamp)}</td>${cells.join("")}</tr>`;
    })
    .join("");

  dataTable.innerHTML = `
    <thead><tr>${header.map((item) => `<th>${item}</th>`).join("")}</tr></thead>
    <tbody>${body}</tbody>
  `;
}

function renderInsights(rows) {
  const insights = selectedCurrencies.map((code) => {
    const first = findFirstRate(rows, code);
    const last = findLastRate(rows, code);
    if (!first || !last) return `${code} has insufficient data in this range.`;

    const multiple = last / first;
    if (multiple >= 20) return `${code} depreciated about ${formatMultiple(multiple)}x across the selected period.`;
    if (multiple >= 5) return `${code} weakened materially, ending around ${formatMultiple(multiple)}x its start-rate level.`;
    if (multiple <= 0.7) return `${code} appreciated versus USD, ending below its start-rate level.`;
    if (multiple <= 1.5) return `${code} remained relatively stable against USD in this range.`;
    return `${code} moved moderately, ending around ${formatMultiple(multiple)}x its start-rate level.`;
  });

  const strongestMove = selectedCurrencies
    .map((code) => ({ code, multiple: (findLastRate(rows, code) || 0) / (findFirstRate(rows, code) || 1) }))
    .filter((item) => Number.isFinite(item.multiple) && item.multiple > 0)
    .sort((a, b) => b.multiple - a.multiple)[0];

  if (strongestMove) {
    insights.unshift(`${strongestMove.code} shows the largest depreciation among the selected currencies.`);
  }

  if (selectedCurrencies.includes("TRY") && Number(endYearSelect.value) >= 2005) {
    insights.push("TRY shows an extreme post-2005 rise, consistent with high inflation and redenomination effects.");
  }

  insightsList.innerHTML = insights.map((insight) => `<li>${insight}</li>`).join("");
}

function buildRows(timestamps, codes) {
  return timestamps.map((timestamp) => {
    const row = { timestamp };
    codes.forEach((code) => {
      const dataPoint = currencyData[code].find((item) => item.timestamp === timestamp);
      row[code] = dataPoint ? dataPoint.rate : null;
    });
    return row;
  });
}

function getSelectedTimestamps() {
  const start = Number(startYearSelect.value);
  const end = Number(endYearSelect.value);
  return getAllTimestamps().filter((timestamp) => {
    const year = new Date(timestamp).getFullYear();
    return year >= start && year <= end;
  });
}

function getAllYears() {
  return [...new Set(getAllTimestamps().map((timestamp) => new Date(timestamp).getFullYear()))].sort((a, b) => a - b);
}

function getAllTimestamps() {
  return [...new Set(Object.values(currencyData).flatMap((series) => series.map((item) => item.timestamp)))].sort(
    (a, b) => new Date(a) - new Date(b)
  );
}

function getSelectedCurrencies() {
  const codes = Array.from(currencySelect.selectedOptions).map((option) => option.value);
  return codes.length ? codes : ["INR"];
}

function setSelectedCurrencies(codes) {
  selectedCurrencies = codes;
  Array.from(currencySelect.options).forEach((option) => {
    option.selected = codes.includes(option.value);
  });
}

function findFirstRate(rows, code) {
  return rows.find((row) => row[code] !== null && row[code] !== undefined)?.[code] || null;
}

function findLastRate(rows, code) {
  return [...rows].reverse().find((row) => row[code] !== null && row[code] !== undefined)?.[code] || null;
}

function getCurrencyColor(code) {
  return currencyMeta[code]?.color || "#ffffff";
}

function updateModeDescription() {
  const descriptions = {
    raw: "Raw exchange rates: 1 USD = X units of selected currency.",
    normalized: "Normalized index: selected start year equals 100 for each currency.",
    log: "Logarithmic Y-axis: useful when high-denomination currencies compress the chart."
  };
  modeDescription.textContent = descriptions[selectedMode];
}

function updateHeadlineQuote(rows) {
  const code = selectedCurrencies[0] || "INR";
  const last = findLastRate(rows, code);
  headlineQuote.textContent = last ? `1 USD = ${formatRate(last)} ${code}` : "Select a currency";
}

