async function loadCurrencyData() {
  try {
    const response = await fetch("data/currency-data.json", { cache: "no-store" });
    if (!response.ok) throw new Error("Currency JSON could not be loaded.");
    return normalizeCurrencyData(await response.json());
  } catch (error) {
    addFallbackNote("Using embedded fallback data because the browser blocked local JSON loading.");
    return normalizeCurrencyData(fallbackCurrencyData);
  }
}

function normalizeCurrencyData(rawData) {
  return Object.fromEntries(
    Object.entries(rawData).map(([code, records]) => [code, normalizeCurrencySeries(records)])
  );
}

function normalizeCurrencySeries(records) {
  const points = Array.isArray(records)
    ? records.map((record) => ({
        timestamp: String(record.timestamp || `${record.year}-01-01`),
        rate: Number(record.rate)
      }))
    : Object.entries(records).map(([timestamp, rate]) => ({
        timestamp,
        rate: Number(rate)
      }));

  return points
    .filter((point) => point.timestamp && Number.isFinite(point.rate))
    .sort((a, b) => new Date(a.timestamp) - new Date(b.timestamp));
}

